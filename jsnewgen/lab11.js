
let [first, second, third] = ['Maya', 'Marisa', 'Chi'];
console.log(first); // *
console.log(second); // **
console.log(third); // ***
//การใช้ console.log ในทั้งสามบรรทัดนี้เป็นการแสดงค่าของตัวแปร first, second, และ third ตามลำดับ ซึ่งได้มาจากการ destructuring array ['Maya', 'Marisa', 'Chi'] ที่กำหนดค่าให้กับตัวแปรทั้งสามตัวนี้.
//ดังนั้นผลลัพธ์ทั้งสามบรรทัดคือ "Maya", "Marisa", และ "Chi" ตามลำดับ.